# Rules of Variables in Python

# 1. You cannot start a variable with a digit
# 15 = 'hello'

# 2. You can start the variable from the alphabet itself 
# a = 'hello'
# a15 = 'hello'

# 3. You cannot use symbol in a variable
# A` = 'hello'
# A~ = 'hello'
# A! = 'hello'
# A@ = 'hello'
# A$ = 'hello'
# A% = 'hello'
# A^ = 'hello'
